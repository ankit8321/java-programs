//* PRINTING  1 to 10 USING FOR LOOP

import java.util.*;
public class java20
{
    public static void main(String args[])
    {
        try(Scanner sc=new Scanner(System.in))
        {
            //& Syntax for(initialization;condition;updation)
            for(int i=0;i<=10;i++)
            {
                System.out.println(i);
            }

        }
    }
}
